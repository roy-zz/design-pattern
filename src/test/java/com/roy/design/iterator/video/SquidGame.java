package com.roy.design.iterator.video;

public class SquidGame {
    private int episode;
    private String name;

    public SquidGame(int episode, String name) {
        this.episode = episode;
        this.name = name;
    }

    public int getEpisode() {
        return episode;
    }

    public String getName() {
        return name;
    }

}
